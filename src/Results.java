    private void btn1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn1ActionPerformed
        // TODO add your handling code here:

        try{

            Class.forName(driverName);
            Connection conn = (Connection) DriverManager.getConnection(url,user,passwd);
            String sql="SELECT * FROM results";
            PreparedStatement pstm=conn.prepareStatement(sql);
            ResultSet rs=pstm.executeQuery();

            model=(DefaultTableModel)view.getModel();

            while(rs.next()){
                int regno=rs.getInt("regno");
                String fullname=rs.getString("fullname");
                Double ca=rs.getDouble("CA");
                Double ue=rs.getDouble("UE");
                Double tscore=rs.getDouble("Tscore");
                String grade;
                if(tscore>=70&&tscore<=100){
                    grade="A";
                }else if(tscore>=60&&tscore<70){
                    grade="B+";
                }else if(tscore>=50&&tscore<60){
                    grade="B";
                }else if(tscore>=40&&tscore<50){
                    grade="C";
                }else if(tscore>=30&&tscore<40){
                    grade="D";
                }else if(tscore>=0&&tscore<30){
                    grade="E";
                }else{
                    grade="Wrong Score";
                }
                model.addRow(new Object[]{regno, fullname, ca, ue, tscore, grade});
                view.setModel(model);
            }
        } catch (ClassNotFoundException | SQLException ex) {
            System.out.print(ex);
        }
    }//GEN-LAST:event_btn1ActionPerformed

    private void btn3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn3ActionPerformed
        try {
            int regno = Integer.parseInt(txt2.getText());
            String fullname = txt3.getText();
            Double ca = Double.parseDouble(txt4.getText());
            Double ue = Double.parseDouble(txt5.getText());
            Double Tscore=ca+ue;

            Class.forName(driverName);
            Connection conn = (Connection) DriverManager.getConnection(url,user,passwd);

            String sql = "UPDATE results SET fullname=?, CA=?, UE=?,"
            + "Tscore=? WHERE regno='"+regno+"'";
            PreparedStatement pst = conn.prepareStatement(sql);
            pst.setString(1, fullname);
            pst.setDouble(2, ca);
            pst.setDouble(3, ue);
            pst.setDouble(4, Tscore);
           
            int c=pst.executeUpdate();
            if(c>=1){
                JOptionPane.showMessageDialog(null, "Data Successfully Updeted...");
                txt2.setText("");
                txt3.setText("");
                txt4.setText("");
                txt5.setText("");
            }else{
                 JOptionPane.showMessageDialog(null, "Action denied...");
            }

        } catch (SQLException | ClassNotFoundException ex) {
            System.out.print(ex);
        }
    }//GEN-LAST:event_btn3ActionPerformed

